#Escribir un programa que me ingrese la base y la altura de un triangulo rectangulo, me permita 
# calcular su área correspondiente a partir de la siguiente formula
#A=(b*h)/2

#Datos de Entrada
base = int(input("Digite la base del triangulo rectángulo "))
altura = int(input("Digite la altura del triangulo rectángulo "))

#Procesamiento
area = (base*altura)/2

#Datos de Salida
print("El área del triangulo rectángulo es ", area)